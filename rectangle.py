def area(a, b):
    """Return area of rectangle with given params

    :param a: first side of rectangle
    :type a: int|float

    :param b: second side of rectangle
    :type b: int|float

    :rtype: int|float
    :return: area of rectangle
    """
    return a * b


def perimeter(a, b):
    """Return perimetr of rectangle with given params

    :param a: first side of rectangle
    :type a: int|float

    :param b: second side of rectangle
    :type b: int|float

    :rtype: int|float
    :return: perimetr of rectangle
    """
    return (a + b) * 2
