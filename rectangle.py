def area(a, b):
    """
    Calculates area for rectangle
    :param float a: first side
    :param float b: second side
    :return float:
    """
    return a * b


def perimeter(a, b):
    """
    Calculates perimeter for rectangle
    :param float a: first side
    :param float b: second side
    :return float:
    """
    return 2 * (a + b)