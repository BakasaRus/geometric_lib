def area(a, b):
    '''
    :param a: the first side of the rectangle
    :param b: the second side of the rectangle
    :return: the area of the rectangle defined by the variables a and b
    '''
    return a * b 


def perimeter(a, b):
    """
    :param a: the first side of the rectangle
    :param b: the second side of the rectangle
    :return: the perimeter of the rectangle defined by the variables a and b
    """
    return 2*a + 2*b 
