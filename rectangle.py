def area(a, b):
    '''
        Принимает числа a и b и возвращает площадь прямоугольника с такими сторонами.
        Пример: area(5,3) = 15
    '''
    return a * b

def perimeter(a, b):
    '''
            Принимает числа a и b и возвращает периметр прямоупергольника с такими сторонами.
            Пример: perimeter(5,3) = 16
    '''
    return a + b + a + b