def area(a, b):
    """
    Calculates the area of a rectangle given the sides lengths.

        Parameters:
            a (int): The length of a side of the rectangle
            b (int): The length of a side of the rectangle

        Returns:
            area (int): The area of the rectangle with a and b sides
    """
    return a*b


def perimeter(a, b):
    """
    Calculates the perimeter of a rectangle given the sides lengths.

        Parameters:
            a (int): The length of a side of a side of the rectangle
            b (int): The length of a side of a side of the rectangle

        Returns:
            perimeter (int): The perimeter of the rectangle with a and b sides
    """
