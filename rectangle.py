def area(a, b):
    '''
    Takes numbers a and b and returns the area of a rectangle with these sides.
    Example: area(5, 3) = 15
    '''
    return a * b

def perimeter(a, b):
    '''
    Takes numbers a and b and returns the perimeter of a rectangle with these sides.
    Example: perimeter(5, 3) = 16
    '''
    return a + b + a + b


