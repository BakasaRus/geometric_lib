import math


def area(r):
    '''
    Returns the area of a circle

        Options:
            r (int/float): circle radius value

        Return value:
            math.pi * r * r (int/float): area of a circle
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Returns the perimeter of a circle

        Options:
            r (int/float): circle radius value

        Return value:
            math.pi * r * r (int/float): perimeter of a circle
    '''
    return 2 * math.pi * r