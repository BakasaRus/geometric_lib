def area(a):
    '''
    Returns the area of a square

        Options:
            a (int/float): square side size

        Return value:
            a * a (int/float): square area size
    '''
    return a * a


def perimeter(a):
    '''
    Returns the perimeter of a square

        Options:
            a (int/float): square side size

        Return value:
            4 * a (int/float): square perimeter size
    '''
    return 4 * a
