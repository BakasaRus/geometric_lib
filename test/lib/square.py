
def area(a):
    '''
    Принимает a (сторону квадрата)
    Возвращает a*a (площадь квадрата)
    Пример:
    Принимает:8
    Возвращает:64
    '''
    return a * a


def perimeter(a):
    '''
    Принимает a (сторону квадрата)
    Возвращает a*4 (периметр квадрата)
    Пример:
    Принимает:8
    Возвращает:32
    '''
    return 4 * a
