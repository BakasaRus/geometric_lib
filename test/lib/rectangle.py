def area(a, b):
    '''
    Принимает a,b (стороны прямоугольника)
    Возвращает a*b (площадь прямоугольника)
    Пример:
    Принимает:4 7
    Возвращает:28
    '''
    return a * b

def perimeter(a, b):
    '''
    Принимает a,b (стороны прямоугольника)
    Возвращает 2*(a+b) (периметр прямоугольника)
    Пример:
    Принимает:4 7куе
    Возвращает:22
    '''
    return 2*(a + b)
