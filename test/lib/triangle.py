def area(a, h):
    '''
    Принимает a(сторона треугольника),h (высота треугольника)
    Возвращает a*h/2 (площадь треугольника)
    Пример:
    Принимает:3 4
    Возвращает:6
    '''
    return a * h / 2

def perimeter(a, b, c):
    '''
    Принимает a,b,c (стороны треугольника)
    Возвращает a+b+c (периметр треугольника)
    Пример:
    Принимает:3 7 9
    Возвращает:19
    '''
    return a + b + c