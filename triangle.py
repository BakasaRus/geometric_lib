def area(a, h):
    """
    Calculates area for triangle
    :param float a: base
    :param float h: height
    :return float:
    """
    return a * h / 2


def perimeter(a, b, c):
    """
    Calculates perimeter for triangle
    :param float a: first side
    :param float b: second side
    :param float c: third side
    :return float:
    """
    return a + b + c
