def area(a, h):
    '''
    Takes numbers a and h and returns the area of a triangle with side a and height h.
    Example: area(5, 4) = 10
    '''
    return a * h / 2

def perimeter(a, b, c):
    '''
    Takes numbers a, b, and c and returns the perimeter of a triangle with these sides.
    Example: perimeter(1, 2, 3) = 6
    '''
    return a + b + c


