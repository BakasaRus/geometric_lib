def area(a, h):
    """Принимает длину стороны треугольника и проведенную к ней высоту, возвращает площадь для такого треугольника"""
    return a * h / 2 

def perimeter(a, b, c):
    """"Принимает длины трех сторон треугольника, возвращает периметр для такого треугольника"""
    return a + b + c 
