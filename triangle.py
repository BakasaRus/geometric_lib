def area(a, h):
    '''
        Принимает числа а и h и возвращает площадь треугольника со стороной а и проведенной к ней высотой h.
        Пример: area(5,4) = 10
    '''
    return a * h / 2

def perimeter(a, b, c):
    '''
        Принимает числа a b c и возвращает периметр треугольника с такими сторонами.
        Пример: perimeter(1,2,3) = 6
    '''
    return a + b + c