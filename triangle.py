def area(a, h):
    '''
    :param a: the side of the triangle base
    :param h: the height of the triangle drawn to the base
    :return: the area of the triangle
    '''
    return a * h / 2


def perimeter(a, b, c):
    """
    :param a: the first side of the triangle
    :param b: the second side of the triangle
    :param c: the third side of the triangle
    :return: the perimeter of a triangle defined by three sides
    """
    return a + b + c

