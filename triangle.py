def area(a, h): 
    '''
    Calculate area for triangle
    
    Parameters: a, h (int/float): side and height
    Return value: a * h / 2 (int/float): area
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''
    Calculate perimeter for triangle
    
    Parameters: a, b, c (int/float): sides
    Return value: a + b + c (int/float): perimeter
    '''
    return a + b + c 
