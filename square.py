
def area(a):
    '''
    Возвращает площадь квадрата со стороной a

        Параметры:
            a (float): сторона квадрата

        Возвращаемое значение:
            a * a  (float): площадь квадрата
    '''
    return a * a


def perimeter(a):
    '''
    Возвращает периметр квадрата со стороной a

        Параметры:
            a (float): сторона квадрата

        Возвращаемое значение:
            4 * a  (float): периметр квадрата
    '''
    return 4 * a
