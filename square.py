
def area(a):
    """Return area of square with given param

    :param a: side of square
    :type a: int|float

    :rtype: int|float
    :return: area of square
    """
    return a * a


def perimeter(a):
    """Return perimeter of square with given param

    :param a: side of square
    :type a: int|float

    :rtype: int|float
    :return: perimeter of square
    """
    return 4 * a
