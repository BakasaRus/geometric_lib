
def area(a):
    """
    Calculates the area of a square given the side length.

        Parameters:
            a (int): The length of sides of the square

        Returns:
            area (int): The area of the square with a sides
    """
    return a * a


def perimeter(a):
    """
    Calculates the perimeter of a square given the side length.

        Parameters:
            a (int): The length of sides of the square

        Returns:
            perimeter (int): The perimeter of the square with a sides
    """
    return 4 * a
