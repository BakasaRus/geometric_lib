def area(a):
    '''
         Принимает число a и возвращает площадь квадрата со стороной а.
         Пример: area(5) = 25
    '''
    return a * a


def perimeter(a):
    '''
        Принимает число а и возвращает периметр квадрата со стороной а.
        Пример: perimeter(5) = 20
    '''
    return 4 * a
