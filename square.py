
def area(a):
    '''
    Calculates area for rectangle
    
    Parameters: a (int/float): side
    Return value: a * a (int/float): area
    '''
    return a * a


def perimeter(a):
    '''
    Calculates perimeter for square
    
    Parameters: a (int/float): side
    Return value: 4 * a (int/float): perimeter
    '''
    return 4 * a
