def area(a):
    '''
    Takes a number a and returns the area of a square with side length a.
    Example: area(5) = 25
    '''
    return a * a

def perimeter(a):
    '''
    Takes a number a and returns the perimeter of a square with side length a.
    Example: perimeter(5) = 20
    '''
    return 4 * a


