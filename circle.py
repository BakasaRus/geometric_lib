import math


def area(r):
    '''
    Calculates area for circle
    
    Parameters: r (int/float): radius
    Return value: math.pi * r * r (int/float): area
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Calculates perimeter for circle
    
    Parameters: r (int/float): radius
    Return value: 2 * math.pi * r (int/float): perimeter
    '''
    return 2 * math.pi * r

