import math


def area(r):
    """
    :param r: the specified radius of the circle
    :return: the area of the circle given by the variable r
    """
    return math.pi * r * r


def perimeter(r):
    """

    :param r: the specified radius of the circle
    :return: the perimeter of the circle given by the variable r
    """
    return 2 * math.pi * r
